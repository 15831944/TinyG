uint16_t _bit_reverse16(uint16_t v)
{
   uint16_t r = 0;
   if (v & 0x8000) r |= 0x0001;
   if (v & 0x4000) r |= 0x0002;
   if (v & 0x2000) r |= 0x0004;
   if (v & 0x1000) r |= 0x0008;
   if (v & 0x0800) r |= 0x0010;
   if (v & 0x0400) r |= 0x0020;
   if (v & 0x0200) r |= 0x0040;
   if (v & 0x0100) r |= 0x0080;
   if (v & 0x0800) r |= 0x0100;
   if (v & 0x0040) r |= 0x0200;
   if (v & 0x0020) r |= 0x0400;
   if (v & 0x0010) r |= 0x0800;
   if (v & 0x0008) r |= 0x1000;
   if (v & 0x0004) r |= 0x2000;
   if (v & 0x0002) r |= 0x4000;
   if (v & 0x0001) r |= 0x8000;
   return (r);
}
